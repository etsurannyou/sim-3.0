function T(n) {
    if (n === 0)
        return 0;
    const log2N = Math.log2(n);
    if (log2N % 1 === 0)
        return (Math.pow(2, (2 * log2N + 1)) + 1) / 3;
    const i = n - Math.pow(2, Math.floor(log2N));
    return T(Math.pow(2, Math.floor(log2N))) + 2 * T(i) + T(i + 1) - 1;
}
